### Operadores Aritméticos ###
print(3 + 4)
print(3 - 4)
print(3 * 4)
print(3 / 4)
print(10 % 3)
print(10 // 3)
print(2 ** 3)

print("Hola " + "Python " + "¿Qué tal?")    
print("Hola " + str(5))
print("Hola " * 5)

### Operadores Comparativos ###
print(3 > 4)
print(3 < 4)
print(3 >= 4)
print(4 <= 4)
print(3 == 4)
print(3 != 4)

### Operadores Lógicos ###
print(3 > 4 and 3 < 4)
print(3 > 4 or 3 < 4)
print(not 3 > 4)
